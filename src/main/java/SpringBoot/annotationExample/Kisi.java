package SpringBoot.annotationExample;

public class Kisi{
	 
	 String ad; 
	 long id;
	 
	 public Kisi() {
	  super();
	 }

	 public Kisi(String ad,long id) {
	  super();
	  this.ad = ad;
	  this.id=id;
	 }
	 
	 public String getAd() {
	  return ad;
	 }

	 public void setAd(String ad) {
	  this.ad = ad;
	 }

	 public long getId() {
	  return id;
	 }

	 public void setId(long id) {
	  this.id = id;
	 } 
	 
	}
